package com.example.demo.level;

/**
 * Enum representing the various types of levels and game states in the application.
 */
public enum LevelType {
    /**
     * Represents the first level of the game.
     */
    LEVEL_ONE,

    /**
     * Represents the second level of the game.
     */
    LEVEL_TWO,

    /**
     * Represents an endless level mode, commonly referred to as "infinity mode."
     */
    LEVEL_INFINITY,

    /**
     * Represents the main menu of the game.
     */
    MAIN_MENU,

    /**
     * Represents the state when the player wins the game.
     */
    WIN,

    /**
     * Represents the state when the game is over.
     */
    GAME_OVER
}
